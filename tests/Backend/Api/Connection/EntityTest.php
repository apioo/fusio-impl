            'foo' => 'bar',
        ]));

        $body = (string) $response->getBody();

        $this->assertEquals(404, $response->getStatusCode(), $body);
    }

    public function testPut()
    {
        $config = [
            'url' => Environment::getConfig('psx_connection'),
        ];

        $metadata = [
            'foo' => 'bar'
        ];

        $response = $this->sendRequest('/backend/connection/1', 'PUT', array(
            'User-Agent'    => 'Fusio TestCase',
            'Authorization' => 'Bearer da250526d583edabca8ac2f99e37ee39aa02a3c076c0edc6929095e20ca18dcf'
        ), json_encode([
            'name'     => 'Foo',
            'class'    => SqlAdvanced::class,
            'config'   => $config,
            'metadata' => $metadata,
        ]));

        $body   = (string) $response->getBody();
        $expect = <<<'JSON'
{
    "success": true,
    "message": "Connection successfully updated",
    "id": "1"
}
JSON;

        $this->assertEquals(200, $response->getStatusCode(), $body);
        $this->assertJsonStringEqualsJsonString($expect, $body, $body);

        // check database
        $sql = $this->connection->createQueryBuilder()
            ->select('id', 'name', 'class', 'config', 'metadata')
            ->from('fusio_connection')
            ->where('id = 1')
            ->setFirstResult(0)
            ->setMaxResults(1)
            ->getSQL();

        $row = $this->connection->fetchAssociative($sql);

        $this->assertEquals(1, $row['id']);
        $this->assertNotEmpty($row['config']);
        $this->assertJsonStringEqualsJsonString(json_encode($metadata), $row['metadata']);

        $projectKey = Environment::getConfig('fusio_project_key');
        $newConfig  = Connection\Encrypter::decrypt($row['config'], $projectKey);

        $this->assertEquals($config, $newConfig);

        // it is not possible to change the name or class so check whether they
        // have not changed
        $this->assertEquals('System', $row['name']);
        $this->assertEquals('Fusio.Impl.Connection.System', $row['class']);
    }

