    public function testPost()
    {
        $response = $this->sendRequest('/backend/bundle', 'POST', array(
            'User-Agent'    => 'Fusio TestCase',
            'Authorization' => 'Bearer da250526d583edabca8ac2f99e37ee39aa02a3c076c0edc6929095e20ca18dcf'
        ), json_encode([
            'name' => 'new-bundle',
            'version' => '0.1.0',
            'icon' => 'info-circle',
            'summary' => 'My new bundle',
            'description' => 'And a long description',
            'cost' => 1337,
            'config' => [
                'actions' => ['2']
            ],
        ]));

        $body   = (string) $response->getBody();
        $expect = <<<'JSON'
{
    "success": true,
    "message": "Bundle successfully created",
    "id": "2"
}
JSON;

        $this->assertEquals(201, $response->getStatusCode(), $body);
        $this->assertJsonStringEqualsJsonString($expect, $body, $body);

        // check database
        $sql = $this->connection->createQueryBuilder()
            ->select('id', 'status', 'name', 'version', 'icon', 'summary', 'description', 'cost', 'config')
            ->from('fusio_bundle')
            ->orderBy('id', 'DESC')
            ->setFirstResult(0)
            ->setMaxResults(1)
            ->getSQL();

        $row = $this->connection->fetchAssociative($sql);

        $config = <<<JSON
{
    "actions": [
        "2"
    ]
}
JSON;

        $this->assertEquals(Table\Bundle::STATUS_ACTIVE, $row['status']);
        $this->assertEquals('new-bundle', $row['name']);
        $this->assertEquals('0.1.0', $row['version']);
        $this->assertEquals('info-circle', $row['icon']);
        $this->assertEquals('My new bundle', $row['summary']);
        $this->assertEquals('And a long description', $row['description']);
        $this->assertEquals(1337, $row['cost']);
        $this->assertJsonStringEqualsJsonString($config, $row['config']);
    }

